package Loops;

public class BasicLoop {
    public static void main(String[] args) {
        for (int i=1; i<=5 ; i++){
            System.out.println("HELLO WORLD");
        }
        int j;
        for ( j=1;j<=8;j++){
            System.out.print(j+"  ");

        }
        System.out.println();
        System.out.println(j);
    }
}
 